function detectArchiveType(name) {
  const n = name.toLowerCase();
  if (n.endsWith('.7z')) return '7z';
  if (n.endsWith('.rar') || /\.r\d{2}$/.test(n) || /\.part\d+\.rar$/.test(n)) return 'rar';
  if (n.endsWith('.zip')) return 'zip';
  if (n.endsWith('.tar.gz') || n.endsWith('.tgz')) return 'tgz';
  if (n.endsWith('.tar')) return 'tar';
  return null;
}

function getRarVolumes(filename) {
  const m1 = filename.match(/^(.*?)\.part(\d+)\.rar$/i);
  if (m1) {
    const base = m1[1];
    const padding = m1[2].length;
    return { base, pattern: 'partN', padding, ext: 'rar' };
  }
  const m2 = filename.match(/^(.*?)\.rar$/i);
  if (m2) return { base: m2[1], pattern: 'r00', ext: 'rar' };
  return null;
}

