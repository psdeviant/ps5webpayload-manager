function validateExtractParams(body) {
  const {
    source = 'smb',
    source_id,
    smb_path = '',
    filename,
    local_path,
    dest_subdir,
    dest_kind: destKindRaw,
    dest_local_path,
  } = body || {};

  if (source !== 'smb' && source !== 'local' && source !== 'local-fs') {
    return { error: 'source must be smb, local, or local-fs' };
  }

  let archivePath;
  let smbSource = null;
  let smbRemoteDir = '';

  ensureMkpfsDir();

  if (source === 'smb') {
    if (!source_id || !filename) return { error: 'source_id and filename required' };
    smbSource = getSource(parseInt(source_id));
    if (!smbSource || smbSource.type !== 'smb') return { error: 'SMB source not found' };
    smbRemoteDir = (smb_path || '').replace(/\\/g, '/').replace(/^\/+/, '').replace(/\/+$/, '');
  } else if (source === 'local-fs') {
    if (!local_path) return { error: 'local_path required' };
    archivePath = path.resolve(local_path);
    if (!isLocalPathAllowed(archivePath)) return { error: 'Path not allowed' };
    if (!fs.existsSync(archivePath) || !fs.statSync(archivePath).isFile()) return { error: 'Archive not found' };
  } else {
    if (!local_path) return { error: 'local_path required' };
    archivePath = path.resolve(mkpfsWorkDir, local_path.replace(/\\/g, '/').replace(/^\/+/, ''));
    if (!isInsideWorkDir(archivePath)) return { error: 'Out of work dir' };
    if (!fs.existsSync(archivePath)) return { error: 'Archive not found' };
  }

  const archiveName = source === 'smb' ? filename : path.basename(archivePath);
  const archiveType = detectArchiveType(archiveName);
  if (!archiveType) return { error: `Unsupported archive type: ${archiveName}` };

  const stripExt = (n) => n.replace(/\.(7z|rar|zip|tar\.gz|tgz|tar|r\d{2}|part\d+\.rar)$/i, '');

  let destKind = destKindRaw;
  if (!destKind) {
    if (source === 'smb') destKind = 'smb-back';
    else if (source === 'local-fs') destKind = 'local-fs';
    else destKind = 'workdir';
  }

  let destLabel;
  let plannedDestBase = null;
  if (destKind === 'smb-back') {
    if (source !== 'smb') return { error: 'smb-back destination requires source=smb' };
    destLabel = `smb://${smbSource.smb_host}/${smbSource.smb_share}${smbRemoteDir ? '/' + smbRemoteDir : ''}`;
  } else if (destKind === 'local-fs') {
    const baseDir = dest_local_path
      ? path.resolve(dest_local_path)
      : (source === 'local-fs' ? path.dirname(archivePath) : mkpfsWorkDir);
    if (!isLocalPathAllowed(baseDir)) return { error: `Path not allowed: ${baseDir}` };
    plannedDestBase = path.join(baseDir, stripExt(archiveName));
    destLabel = plannedDestBase;
  } else {
    plannedDestBase = dest_subdir
      ? path.resolve(mkpfsWorkDir, dest_subdir.replace(/\\/g, '/').replace(/^\/+/, ''))
      : path.join(mkpfsWorkDir, stripExt(archiveName));
    if (!isInsideWorkDir(plannedDestBase)) return { error: 'destination out of work dir' };
    destLabel = plannedDestBase;
  }

  return { source, smbSource, smbRemoteDir, archivePath, archiveName, archiveType, destKind, destLabel, plannedDestBase };
}

function buildExtractJob(body, prep) {
  return {
    id: newJobId(),
    type: 'extract',
    source: prep.source,
    dest_kind: prep.destKind,
    archive: prep.archiveName,
    dest: prep.destLabel,
    archive_type: prep.archiveType,
    status: 'running',
    log: '',
    progress: 0,
    started_at: new Date().toISOString(),
    finished_at: null,
    exit_code: null,
    error: null,
    _params: body,
    _prep: prep,
  };
}

async function executeExtractJob(job) {
  const { source, smbSource, smbRemoteDir, archivePath: prepArchivePath, archiveName,
    archiveType, destKind, plannedDestBase } = job._prep;
  const { filename, password = '', delete_archive_after = false } = job._params || {};

  let workingArchive = prepArchivePath;
  let smbDownloadDir = null;
  let smbBackTmpDir = null;
  let destBase = plannedDestBase;
  let smbFiles = source === 'smb' ? [filename] : [];

  try {
    if (destKind === 'smb-back') {
      smbBackTmpDir = fs.mkdtempSync(path.join(getDiskTmpRoot(), 'mm-extract-out-'));
      destBase = smbBackTmpDir;
    } else {
      try { fs.mkdirSync(destBase, { recursive: true }); }
      catch (e) {
        job.status = 'failed';
        job.error = `Cannot create dest: ${e.message}`;
        job.finished_at = new Date().toISOString();
        appendLog(job, `[manager] ERROR: ${job.error}\n`);
        return;
      }
    }

    if (source === 'smb') {
      smbDownloadDir = fs.mkdtempSync(path.join(getDiskTmpRoot(), 'mm-extract-'));
      appendLog(job, `[manager] Listing remote dir ${smbRemoteDir || '/'} for related volumes...\n`);
      let remoteFiles = [];
      try {
        remoteFiles = await listSmbDir(smbSource, smbRemoteDir);
      } catch (e) {
        appendLog(job, `[manager] List failed: ${e.message} (continuing with single file)\n`);
      }

      if (archiveType === 'rar') {
        const vol = getRarVolumes(filename);
        if (vol) {
          if (vol.pattern === 'partN') {
            const re = new RegExp(`^${vol.base.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\.part(\\d+)\\.rar$`, 'i');
            smbFiles = remoteFiles.filter(f => !f.isDir && re.test(f.name)).map(f => f.name).sort();
          } else {
            const re = new RegExp(`^${vol.base.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\.(rar|r\\d{2,3})$`, 'i');
            smbFiles = remoteFiles.filter(f => !f.isDir && re.test(f.name)).map(f => f.name).sort();
            if (smbFiles.length === 0) smbFiles = [filename];
          }
        }
      } else if (archiveType === '7z') {
        const m = filename.match(/^(.*?)\.7z\.(\d+)$/i);
        if (m) {
          const base = m[1];
          const re = new RegExp(`^${base.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\.7z(\\.\\d+)?$`, 'i');
          smbFiles = remoteFiles.filter(f => !f.isDir && re.test(f.name)).map(f => f.name).sort();
        }
      }

      if (smbFiles.length === 0) smbFiles = [filename];
      appendLog(job, `[manager] Will download ${smbFiles.length} file(s): ${smbFiles.join(', ')}\n`);
      await downloadSmbFiles(smbSource, smbRemoteDir, smbFiles, smbDownloadDir, job);
      workingArchive = path.join(smbDownloadDir, filename);
    }

    appendLog(job, `[manager] Extracting ${path.basename(workingArchive)} (${archiveType}) → ${destBase}\n`);

    let cmd, args;
    if (archiveType === '7z' || archiveType === 'zip') {
      cmd = '7z';
      // -bsp1 emits "XX%" progress to stdout so the manager can show it in the queue
      args = ['x', workingArchive, `-o${destBase}`, '-y', '-bb1', '-bsp1'];
      if (password) args.splice(2, 0, `-p${password}`);
    } else if (archiveType === 'rar') {
      if (await hasBin('unrar')) {
        cmd = 'unrar';
        args = ['x', '-o+', '-y'];
        if (password) args.push(`-p${password}`); else args.push('-p-');
        args.push(workingArchive, destBase + '/');
      } else if (await hasBin('unar')) {
        cmd = 'unar';
        args = ['-o', destBase, '-f'];
        if (password) args.push('-p', password);
        args.push(workingArchive);
      } else {
        cmd = '7z';
        args = ['x', workingArchive, `-o${destBase}`, '-y'];
        if (password) args.splice(2, 0, `-p${password}`);
      }
    } else if (archiveType === 'tgz') {
      cmd = 'tar';
      args = ['-xzvf', workingArchive, '-C', destBase];
    } else if (archiveType === 'tar') {
      cmd = 'tar';
      args = ['-xvf', workingArchive, '-C', destBase];
    }

    const result = await spawnIntoJob(job, cmd, args);
    job.exit_code = result.code;
    job.finished_at = new Date().toISOString();

    if (result.code === 0) {
      if (destKind === 'smb-back') {
        appendLog(job, `[manager] Uploading extracted contents back to SMB...\n`);
        const upRes = await uploadDirToSmb(job, smbSource, destBase, smbRemoteDir);
        if (upRes.code !== 0) {
          job.status = 'failed';
          job.error = upRes.error || `smb upload exit ${upRes.code}`;
          return;
        }
        appendLog(job, `[manager] SMB upload OK\n`);
      }
      job.status = 'completed';
      appendLog(job, `[manager] Done.\n`);

      if (delete_archive_after) {
        if (source === 'local' && fs.existsSync(prepArchivePath)) {
          try { fs.unlinkSync(prepArchivePath); appendLog(job, `[manager] Deleted local archive: ${path.basename(prepArchivePath)}\n`); }
          catch (e) { appendLog(job, `[manager] Delete failed: ${e.message}\n`); }
        } else if (source === 'local-fs' && isLocalPathAllowed(prepArchivePath) && fs.existsSync(prepArchivePath)) {
          try { fs.unlinkSync(prepArchivePath); appendLog(job, `[manager] Deleted local archive: ${prepArchivePath}\n`); }
          catch (e) { appendLog(job, `[manager] Delete failed: ${e.message}\n`); }
        } else if (source === 'smb') {
          const delArgs = buildSmbArgs(smbSource);
          const cleanRemote = smbRemoteDir;
          const cmds = [];
          if (cleanRemote) cmds.push(`cd "${cleanRemote}"`);
          for (const f of smbFiles) cmds.push(`del "${f}"`);
          delArgs.push('-c', cmds.join('; '));
          appendLog(job, `[manager] Deleting ${smbFiles.length} SMB file(s): ${smbFiles.join(', ')}\n`);
          const { stdout, stderr, code } = await runSmbClient(delArgs);
          const out = stdout + stderr;
          const err = smbClientError(out, code);
          if (err) appendLog(job, `[manager] SMB delete warning: ${err.message}\n`);
          else appendLog(job, `[manager] SMB delete OK\n`);
        }
      }
    } else if (job.status !== 'cancelled') {
      job.status = 'failed';
      job.error = result.error || `exit ${result.code}`;
    }
  } catch (e) {
    job.status = 'failed';
    job.error = e.message;
    job.finished_at = new Date().toISOString();
    appendLog(job, `[manager] ERROR: ${e.message}\n`);
  } finally {
    if (smbDownloadDir) {
      try { fs.rmSync(smbDownloadDir, { recursive: true, force: true }); appendLog(job, `[manager] Cleaned up tmp\n`); }
      catch (_) {}
    }
    if (smbBackTmpDir) {
      try { fs.rmSync(smbBackTmpDir, { recursive: true, force: true }); appendLog(job, `[manager] Cleaned up extract tmp\n`); }
      catch (_) {}
    }
    log('info', `extract ${job.id} ${job.status}`);
  }
}

