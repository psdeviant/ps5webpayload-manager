const PAYLOAD_EXT_REGEX = /\.(elf|lua|prx|sprx|bin)$/i;

function upsertPayloadRow(name, filepath, sourceUrl, size, version) {
  const repo = getRepo();
  const id = repo.queryScalar('SELECT id FROM payloads WHERE name = ? OR filename = ?', [name, name]);
  if (id != null) {
    repo.run(
      'UPDATE payloads SET filename = ?, filepath = ?, source_url = ?, size = ?, version = ?, updated_at = CURRENT_TIMESTAMP WHERE id = ?',
      [name, filepath, sourceUrl, size, version, id],
    );
  } else {
    repo.run(
      'INSERT INTO payloads (name, filename, filepath, source_url, size, version) VALUES (?, ?, ?, ?, ?, ?)',
      [name, name, filepath, sourceUrl, size, version],
    );
  }
  repo.save();
}

async function downloadReleaseAssets(release) {
  ensurePayloadsDir();
  const version = release.tag_name || release.name || 'latest';
  const downloaded = [];
  for (const asset of (release.assets || [])) {
    if (!ASSET_REGEX.test(asset.name)) continue;
    const buf = await downloadAssetBuffer(asset.browser_download_url);

    if (/\.zip$/i.test(asset.name)) {
      try {
        const zip = new AdmZip(buf);
        for (const entry of zip.getEntries()) {
          if (entry.isDirectory) continue;
          if (!PAYLOAD_EXT_REGEX.test(entry.entryName)) continue;
          const entryBuf = entry.getData();
          const filename = entry.entryName.split('/').pop();
          const filepath = path.join(payloadsDir, filename);
          fs.writeFileSync(filepath, entryBuf);
          upsertPayloadRow(filename, filepath, asset.browser_download_url, entryBuf.length, version);
          downloaded.push({ name: filename, size: entryBuf.length, version, from_zip: asset.name });
          log('info', `MicroMount release ${version}: extracted ${filename} from ${asset.name} (${entryBuf.length} bytes)`);
        }
      } catch (e) {
        log('error', `MicroMount: failed to extract ${asset.name}: ${e.message}`);
      }
    } else {
      const filepath = path.join(payloadsDir, asset.name);
      fs.writeFileSync(filepath, buf);
      upsertPayloadRow(asset.name, filepath, asset.browser_download_url, buf.length, version);
      downloaded.push({ name: asset.name, size: buf.length, version });
      log('info', `MicroMount release ${version}: downloaded ${asset.name} (${buf.length} bytes)`);
    }
  }
  return { version, published_at: release.published_at, downloaded };
}

async function checkRelease({ autoDownload = false } = {}) {
  const release = await fetchLatestRelease();
  const latestVersion = release.tag_name || release.name || 'latest';
  const state = loadReleaseState();
  const updateAvailable = !state.version || state.version !== latestVersion;
  const result = {
    current_version: state.version,
    latest_version: latestVersion,
    latest_published_at: release.published_at,
    update_available: updateAvailable,
    assets: (release.assets || []).filter(a => ASSET_REGEX.test(a.name)).map(a => ({ name: a.name, size: a.size })),
    downloaded: null,
  };
  state.checked_at = new Date().toISOString();
  if (updateAvailable && autoDownload) {
    const dl = await downloadReleaseAssets(release);
    state.version = dl.version;
    state.published_at = dl.published_at;
    state.last_update_at = new Date().toISOString();
    state.assets = dl.downloaded.map(d => d.name);
    result.downloaded = dl;
    result.current_version = state.version;
    result.update_available = false;
  }
  saveReleaseState(state);
  return result;
}

