// ─── Queue persistence ───────────────────────────────────────────────────────
//
// All three queues (extract / convert / ftp upload) plus their `paused` flags
// were previously in-memory only, so a `docker compose up -d --build` or any
// container restart silently dropped every queued job.
//
// We now mirror the state to /app/data/queue-state.json — the data dir is
// the only persistent bind mount (see docker-compose.yml), so it survives:
//
//   docker compose restart app
//   docker compose up -d
//   docker compose up -d --build app
//   docker compose down && docker compose up -d
//
// Strategy:
//   - restoreQueueState() runs once at module load (below)
//   - saveQueueState() runs every QUEUE_AUTOSAVE_MS via setInterval
//   - saveQueueState() also runs on SIGTERM/SIGINT (the signal docker sends
//     before killing the container on `up --build`), so the final state lands
//   - Items in transient statuses at save time (running / staging / pushing)
//     are demoted to 'queued' on restore so the workers re-execute them
//
// History (completed/failed/cancelled) is capped so the file does not grow
// without bound; oldest finished items beyond the cap are dropped.
const QUEUE_STATE_FILE = path.join(dataDir, 'queue-state.json');
// Autosave cadence for the queue snapshot. Bumped from 5 → 10 s because
// scheduleQueueSave() is already invoked synchronously on every queue
// mutation (debounced 250 ms) — this periodic save is just a safety net
// for in-flight progress / phase mutations the workers do without going
// through scheduleQueueSave(). The de-dupe in saveQueueState() means an
// idle box writes the file at most once at startup and then never again.
const QUEUE_AUTOSAVE_MS = 10_000;
const QUEUE_HISTORY_KEEP = 100;
const QUEUE_TRANSIENT_STATUSES = new Set([
  'running', 'starting', 'staging', 'pushing', 'unpacking', 'queueing',
]);
let _queuePersistReady = false;

function _stripTransientItem(item) {
  // Drop closure refs that aren't JSON-serializable; everything else (params,
  // paths, status, timestamps, progress) is plain data.
  const { _job, _proc, _outputPoller, ...rest } = item;
  return rest;
}

function _normalizeForSave(items) {
  const live = [];
  const finished = [];
  for (const raw of items) {
    const it = _stripTransientItem(raw);
    if (['completed', 'failed', 'cancelled', 'push_failed'].includes(it.status)) {
      finished.push(it);
    } else {
      live.push(it);
    }
  }
  const trimmedFinished = finished
    .sort((a, b) => String(b.finished_at || '').localeCompare(String(a.finished_at || '')))
    .slice(0, QUEUE_HISTORY_KEEP);
  return [...live, ...trimmedFinished];
}

// Map persistence-key → JobQueue instance. Lazy getters because the queues
// are declared further up the file but persistence must remain referentially
// neutral if/when they get hoisted into a controller class.
const PERSIST_QUEUES = [
  ['extract',   () => extractQ],
  ['convert',   () => convertQ],
  ['ftpUpload', () => ftpUploadQ],
  ['install',   () => installQ],
];

function _serializeQueues() {
  const out = { version: 1, saved_at: new Date().toISOString() };
  for (const [key, qref] of PERSIST_QUEUES) {
    const q = qref();
    out[key] = { paused: q.paused, items: _normalizeForSave(q.items) };
  }
  return out;
}

function saveQueueState() {
  if (!_queuePersistReady) return;
  try {
    // Serialise once, then skip the actual disk write when the payload is
    // byte-identical to the last successfully saved state. `saved_at`
    // changes on every call, so we strip it from the fingerprint window —
    // otherwise the timestamp alone would defeat the cache.
    const snapshot = _serializeQueues();
    const body = JSON.stringify(snapshot, (k, v) => (k === 'saved_at' ? '' : v));
    const fp = _fingerprintJson(body);
    if (fp === _lastQueueFingerprint) return;

    fs.mkdirSync(path.dirname(QUEUE_STATE_FILE), { recursive: true });
    const tmp = `${QUEUE_STATE_FILE}.tmp`;
    // Pretty-printed full snapshot (with saved_at) for the on-disk copy —
    // makes the file diff-friendly when the user inspects /data manually.
    fs.writeFileSync(tmp, JSON.stringify(snapshot, null, 2));
    fs.renameSync(tmp, QUEUE_STATE_FILE);
    _lastQueueFingerprint = fp;
  } catch (e) {
    log('warn', `[queue-persist] save failed: ${e.message}`);
  }
}

let _queueSaveTimer = null;
function scheduleQueueSave(delay = 250) {
  if (!_queuePersistReady) return;
  if (_queueSaveTimer) clearTimeout(_queueSaveTimer);
  _queueSaveTimer = setTimeout(() => { _queueSaveTimer = null; saveQueueState(); }, delay);
}

// Lightweight change detector for the periodic snapshot. saveQueueState()
// previously fired every QUEUE_AUTOSAVE_MS regardless of whether anything
// changed since the last save — on an idle box that's still ~17k disk
// writes per day for no benefit. We now serialize once into a buffer,
// fingerprint it (length + xor-rolled hash), and only commit to disk when
// the fingerprint differs from the last successful write.
//
// Plain Buffer.length+rolling-xor is good enough here: a state of size N
// with a single field flipped will always change either the length or
// the hash; we don't need cryptographic strength.
let _lastQueueFingerprint = '';
function _fingerprintJson(text) {
  let h = 0;
  // 4096-byte stride is enough to detect every realistic change while
  // staying O(N/stride) — full-hash on a 50 KB queue state is ~12 ops.
  for (let i = 0; i < text.length; i += 4096) {
    h = ((h << 5) - h + text.charCodeAt(i)) | 0;
  }
  return `${text.length}:${h}`;
}

function restoreQueueState() {
  try {
    if (!fs.existsSync(QUEUE_STATE_FILE)) {
      log('info', '[queue-persist] no saved state, starting fresh');
      return;
    }
    const raw = fs.readFileSync(QUEUE_STATE_FILE, 'utf8');
    const data = JSON.parse(raw);
    const demote = (it) => {
      if (QUEUE_TRANSIENT_STATUSES.has(it.status)) {
        return {
          ...it,
          status: 'queued',
          started_at: null,
          finished_at: null,
          job_id: null,
          progress: 0,
          phase: null,
          unpack_phase: null,
          error: null,
          exit_code: null,
        };
      }
      // Normalize terminal items so they render cleanly in the UI:
      //   - completed: pin progress=100 (otherwise older saves would show
      //     "Completed · 95% · extracting" because the unpack poller caps
      //     at 95% by design)
      //   - failed / cancelled / push_failed: clear stale phase but keep
      //     the last-known progress so the bar still shows "where we were"
      //     when it died — useful for inferring how far the job got.
      if (it.status === 'completed') {
        return { ...it, progress: 100, phase: null, unpack_phase: null };
      }
      if (['failed', 'cancelled', 'push_failed'].includes(it.status)) {
        return { ...it, phase: null, unpack_phase: null };
      }
      return it;
    };

    const stats = { extract: 0, convert: 0, ftpUpload: 0, install: 0, demoted: 0 };
    for (const [key, qref] of PERSIST_QUEUES) {
      const slice = data?.[key];
      if (!slice?.items) continue;
      const q = qref();
      q.paused = !!slice.paused;
      for (const r of slice.items) {
        const it = demote(r);
        if (it.status !== r.status) stats.demoted++;
        q.items.push(it);
        stats[key]++;
      }
    }
    log('info',
      `[queue-persist] restored: extract=${stats.extract} convert=${stats.convert} ` +
      `ftp=${stats.ftpUpload} install=${stats.install} demoted_running=${stats.demoted} ` +
      `paused: extract=${extractQ.paused} convert=${convertQ.paused} ftp=${ftpUploadQ.paused} install=${installQ.paused}`
    );
  } catch (e) {
    log('warn', `[queue-persist] restore failed (starting fresh): ${e.message}`);
  }
}

restoreQueueState();
_queuePersistReady = true;
// Periodic snapshot picks up status/progress mutations made by the workers
// (which we don't instrument explicitly). On a slow conversion this also
// gives us a recent progress checkpoint in case of a power loss.
setInterval(saveQueueState, QUEUE_AUTOSAVE_MS).unref?.();
// docker stop -> SIGTERM, then SIGKILL 10s later. Save first so the
// `up --build` cycle keeps the latest queue state.
for (const sig of ['SIGTERM', 'SIGINT']) {
  // .once so multiple sources of SIGTERM (Express's own handler, dumb-init's
  // re-raise) don't double-write.
  process.once(sig, () => {
    try { saveQueueState(); } catch (_) {}
  });
}

