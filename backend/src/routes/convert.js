// Periodically send a NOOP on a *separate* FTP control channel so the PS5
// homebrew FTP daemon (and any NAT in between) sees regular activity. The PS5
// can drop into rest mode after extended single-channel inactivity which kills
// long uploads — this keeps it visibly busy.
function startFtpHeartbeat(ip, ftpOpts, intervalMs = 25_000) {
  let stopped = false;
  let client = null;
  let busy = false;

  const tick = async () => {
    if (stopped || busy) return;
    busy = true;
    try {
      if (!client) {
        client = new FtpClient(8000);
        client.ftp.verbose = false;
        await client.access({
          host: ip,
          port: ftpOpts.port,
          user: ftpOpts.username || 'anonymous',
          password: ftpOpts.password || '',
          secure: false,
        });
        try { client.ftp.socket.setKeepAlive(true, 10_000); } catch (_) {}
      }
      await client.send('NOOP');
    } catch (_) {
      try { client?.close(); } catch (_) {}
      client = null;
    } finally {
      busy = false;
    }
  };

  const timer = setInterval(tick, intervalMs);
  setTimeout(tick, 5_000);

  return {
    stop: () => {
      if (stopped) return;
      stopped = true;
      clearInterval(timer);
      try { client?.close(); } catch (_) {}
      client = null;
    },
  };
}

// Resilient FTP upload with TCP keepalive, NOOP heartbeat, and automatic
// resume via APPE on connection drop. The PS5 FTP daemon often dies mid-stream
// when the console enters rest mode — without resume, the entire transfer is
// wasted; with this helper it picks up from the last successfully written byte
// after reconnect.
async function uploadFileResilient(ip, ftpOpts, localPath, destDir, remoteName, opts = {}) {
  const total = fs.statSync(localPath).size;
  const maxRetries = opts.maxRetries ?? 5;
  const onProgress = opts.onProgress || (() => {});
  const onLog = opts.onLog || (() => {});
  const enableHeartbeat = opts.heartbeat !== false;

  let attempt = 0;
  let lastError = null;

  while (attempt < maxRetries) {
    attempt++;
    const hb = enableHeartbeat ? startFtpHeartbeat(ip, ftpOpts) : null;
    try {
      await withFtp(ip, ftpOpts, async (client) => {
        await ensureRemoteDir(client, destDir);
        await client.cd(destDir);

        let startOffset = 0;
        if (attempt === 1) {
          // First attempt: enforce a clean overwrite.
          try { await client.remove(remoteName); } catch (_) {}
        } else {
          // Retry attempt: see how much already made it across.
          try {
            const sz = await client.size(remoteName);
            if (sz >= total) {
              // Looks complete (or oversize from a corrupt prior run) — restart.
              try { await client.remove(remoteName); } catch (_) {}
              startOffset = 0;
            } else if (sz > 0) {
              startOffset = sz;
            }
          } catch (_) { startOffset = 0; }
        }

        if (startOffset > 0) {
          onLog(`[manager] Resuming at ${startOffset}/${total} bytes (attempt ${attempt})\n`);
          onProgress({ bytes: startOffset, total });
          const stream = fs.createReadStream(localPath, { start: startOffset });
          let bytes = startOffset;
          stream.on('data', (chunk) => {
            bytes += chunk.length;
            onProgress({ bytes, total });
          });
          await client.appendFrom(stream, remoteName);
        } else {
          if (attempt > 1) onLog(`[manager] Restarting upload from 0 (attempt ${attempt})\n`);
          client.trackProgress((info) => {
            if (info && typeof info.bytes === 'number') onProgress({ bytes: info.bytes, total });
          });
          try {
            await client.uploadFrom(localPath, remoteName);
          } finally {
            client.trackProgress();
          }
        }
      });
      onProgress({ bytes: total, total });
      return;
    } catch (e) {
      lastError = e;
      onLog(`[manager] WARN: attempt ${attempt} failed: ${e.message}\n`);
      if (attempt >= maxRetries) break;
      const wait = Math.min(15_000, 2_000 * attempt);
      onLog(`[manager] Waiting ${Math.round(wait / 1000)}s then retrying...\n`);
      await new Promise((r) => setTimeout(r, wait));
    } finally {
      if (hb) hb.stop();
    }
  }
  throw lastError || new Error('Upload failed');
}

