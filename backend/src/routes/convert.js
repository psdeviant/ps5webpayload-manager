// PFS inode-bits default. mkpfs's own default is 32 for both PS4 and PS5 (see
// `mkpfs pack file --help`), and 32-bit inodes are what the PS5 PFS reader
// expects for compressed (.ffpfsc) game packs — 64-bit images don't mount on
// retail firmware. We pass it explicitly so:
//   1. the job.command line shown in the Queue UI matches the canonical
//      reference command from the docs (mkpfs pack file --verify
//      --version PS5 --inode-bits 32 …) byte-for-byte,
//   2. a future mkpfs release that flips the default can't silently break
//      our output on existing installs.
const DEFAULT_INODE_BITS = '32';

// PS5 PFS reader requires game files (eboot.bin, sce_sys/, …) at the image
// root with no wrapper folder. The docs spell it out explicitly:
//
//   "The exFAT image must contain the game files at the image root, without
//    an extra top-level folder."
//
// The same rule applies to `mkpfs pack folder` since the folder *becomes*
// the image root. Users frequently stage their source as
// `MyGame/MyGame/eboot.bin` (the outer folder is just a name carrier) and
// pointing mkpfs at the outer folder produces an unmountable image where
// the PS5 sees `image:/MyGame/eboot.bin` instead of `image:/eboot.bin`.
//
// `resolveGameRootForPack` looks at the source directory and, if it doesn't
// look like a game root itself but contains exactly one real subdirectory
// that does, transparently promotes the inner directory. The promotion is
// logged so the user can see what we did in the Queue log.

const GAME_ROOT_FILE_MARKERS = ['eboot.bin', 'EBOOT.BIN', 'eboot.elf'];
const GAME_ROOT_DIR_MARKERS = ['sce_sys', 'SCE_SYS'];

function _looksLikeGameRoot(p) {
  try {
    for (const name of GAME_ROOT_FILE_MARKERS) {
      try {
        if (fs.statSync(path.join(p, name)).isFile()) return true;
      } catch (_) {}
    }
    for (const name of GAME_ROOT_DIR_MARKERS) {
      try {
        if (fs.statSync(path.join(p, name)).isDirectory()) return true;
      } catch (_) {}
    }
  } catch (_) {}
  return false;
}

// Skip hidden / metadata folders that macOS / Windows leave around in
// archives so a single real subdir isn't disguised by `.DS_Store` &c.
function _isMetadataEntry(name) {
  return name.startsWith('.') || name.startsWith('__MACOSX') || name === 'System Volume Information';
}

function resolveGameRootForPack(srcPath) {
  let stat;
  try {
    stat = fs.statSync(srcPath);
  } catch (_) {
    return { root: srcPath, promoted: false, message: null };
  }
  if (!stat.isDirectory()) {
    return { root: srcPath, promoted: false, message: null };
  }
  if (_looksLikeGameRoot(srcPath)) {
    return { root: srcPath, promoted: false, message: null };
  }
  let entries;
  try {
    entries = fs.readdirSync(srcPath, { withFileTypes: true });
  } catch (_) {
    return { root: srcPath, promoted: false, message: null };
  }
  const realSubdirs = entries.filter(e => e.isDirectory() && !_isMetadataEntry(e.name));
  const realFiles = entries.filter(e => !e.isDirectory() && !_isMetadataEntry(e.name));

  // Single subdirectory + no loose files at the outer level → almost
  // certainly a wrapper. Promote if the inner dir is a real game root.
  if (realSubdirs.length === 1 && realFiles.length === 0) {
    const inner = path.join(srcPath, realSubdirs[0].name);
    if (_looksLikeGameRoot(inner)) {
      return {
        root: inner,
        promoted: true,
        message: (
          `Source "${path.basename(srcPath)}/" looked like a wrapper directory ` +
          `(no eboot.bin / sce_sys at root, only "${realSubdirs[0].name}/" inside). ` +
          `Auto-promoted source to "${path.basename(srcPath)}/${realSubdirs[0].name}/" ` +
          `so the resulting .ffpfsc has game files at the image root ` +
          `(PS5 PFS reader requires no extra top-level folder).`
        ),
      };
    }
  }

  // Couldn't find a recognisable game root. Don't fail — some homebrew /
  // unsigned dumps have custom layouts that still pack into a valid PFS —
  // but emit a loud warning so the user knows what to check if the
  // produced .ffpfsc refuses to mount on the PS5.
  return {
    root: srcPath,
    promoted: false,
    message: (
      `WARNING: source "${path.basename(srcPath)}/" does not have "eboot.bin" ` +
      `or "sce_sys/" at its root. PS5 PFS reader requires those at the image ` +
      `root with no wrapper folder. If the resulting .ffpfsc fails to mount, ` +
      `restructure the source so game files sit directly under the chosen folder.`
    ),
  };
}

async function runPackFile(job, opts) {
  const args = ['pack', 'file'];
  args.push(opts.compress ? '--compress' : '--no-compress');
  if (opts.verify) args.push('--verify');
  if (opts.version) args.push('--version', opts.version);
  args.push('--inode-bits', String(opts.inode_bits || DEFAULT_INODE_BITS));
  if (opts.block_size) args.push('--block-size', String(opts.block_size));
  if (opts.compression_level !== undefined && opts.compression_level !== null && opts.compression_level !== '') {
    args.push('--compression-level', String(opts.compression_level));
  }
  args.push('--verbose');
  args.push(job.source, job.output);
  job.command = `${MKPFS_BIN} ${args.join(' ')}`;
  return spawnIntoJob(job, MKPFS_BIN, args, mkpfsEnv(job));
}

async function runPackFolder(job, opts) {
  const args = ['pack', 'folder'];
  args.push(opts.compress ? '--compress' : '--no-compress');
  if (opts.verify) args.push('--verify');
  if (opts.version) args.push('--version', opts.version);
  args.push('--inode-bits', String(opts.inode_bits || DEFAULT_INODE_BITS));
  if (opts.block_size) args.push('--block-size', String(opts.block_size));
  if (opts.compression_level !== undefined && opts.compression_level !== null && opts.compression_level !== '') {
    args.push('--compression-level', String(opts.compression_level));
  }
  if (opts.case_sensitive) args.push('--case-sensitive');
  if (opts.skip_executable_compression) args.push('--skip-executable-compression');
  if (opts.signed) args.push('--signed');
  if (opts.require_game_files) args.push('--require-game-files');
  args.push('--verbose');
  args.push(job.source, job.output);
  job.command = `${MKPFS_BIN} ${args.join(' ')}`;
  return spawnIntoJob(job, MKPFS_BIN, args, mkpfsEnv(job));
}

// Reverse operation — extracts a .ffpfsc / .ffpfs / .pfs / .dat / .bin image
// back into a folder. mkpfs CLI: `mkpfs unpack [--overwrite] [--ekpfs-key K]
// [--new-crypt] image_file output_dir`.
async function runUnpack(job, opts) {
  const args = ['unpack'];
  // Always overwrite — buildConvertJob already wipes the output path before
  // dispatch when it exists, so this avoids mkpfs refusing on a stale dir.
  args.push('--overwrite');
  if (opts.ekpfs_key) args.push('--ekpfs-key', String(opts.ekpfs_key));
  if (opts.new_crypt) args.push('--new-crypt');
  args.push(job.source, job.output);
  job.command = `${MKPFS_BIN} ${args.join(' ')}`;

  // mkpfs unpack is completely silent (no progress, no -v flag exists), so the
  // queue UI would otherwise stay at "running 0%" for the entire decode.
  // Poll the output directory size instead and surface bytes_written +
  // a clamped progress estimate based on the compressed source size.
  let sourceSize = 0;
  try { sourceSize = fs.statSync(job.source).size; } catch (_) {}
  appendLog(job, `[manager] mkpfs unpack is silent; polling output dir for progress (source=${sourceSize} bytes)\n`);
  startOutputDirPoller(job, sourceSize);
  try {
    return await spawnIntoJob(job, MKPFS_BIN, args, mkpfsEnv(job));
  } finally {
    stopOutputDirPoller(job);
  }
}

// Modes:
//   pack-file     — pack a single .exfat/.ffpkg → .ffpfsc (mkpfs)
//   pack-folder   — pack a game-dump folder    → .ffpfsc (mkpfs)
//   unpack        — reverse: .ffpfsc → folder (also supports .ffpfs, .pfs, …)
//   exfat-file    — wrap a single file       → .exfat (mkfs.exfat + loop mount)
//   exfat-folder  — wrap a game-dump folder → .exfat
//   exfat-unpack  — reverse: .exfat → folder (loop mount + rsync)
const CONVERT_MODES = ['pack-file', 'pack-folder', 'unpack', 'exfat-file', 'exfat-folder', 'exfat-unpack'];
const UNPACKABLE_EXT_RE = /\.(ffpfs|ffpfsc|pfs|dat|bin)$/i;
// .exfat is its own unpack source — kept in a separate regex so we don't
// accidentally route .exfat into mkpfs (which would happily start and then
// emit a misleading "header invalid" error).
const EXFAT_EXT_RE = /\.exfat$/i;

function validateConvertParams(params) {
  const {
    mode = 'pack-file',
    source_path,
    source_ftp,
    source_smb,
  } = params;
  if (!CONVERT_MODES.includes(mode)) return { error: 'invalid mode' };
  ensureMkpfsDir();

  // Compute a sensible default output name for the current mode.
  // PFS pack:        Game.exfat / Game.ffpkg  → Game.ffpfsc
  // PFS unpack:      Game.ffpfsc               → Game/         (folder, no suffix)
  // exFAT pack:      Game.iso / Game (folder) → Game.exfat
  // exFAT unpack:    Game.exfat                → Game/         (folder, no suffix)
  // Keeping the names symmetric so a round-trip pack ↔ unpack lands back on
  // the same basename.
  const defaultOutName = (srcBase) => {
    if (mode === 'unpack') {
      return srcBase.replace(UNPACKABLE_EXT_RE, '');
    }
    if (mode === 'exfat-unpack') {
      return srcBase.replace(EXFAT_EXT_RE, '');
    }
    if (mode === 'exfat-file' || mode === 'exfat-folder') {
      return srcBase.replace(/\.(exfat|ffpkg|ffpfsc|iso|img|bin)$/i, '') + '.exfat';
    }
    return srcBase.replace(/\.(exfat|ffpkg|ffpfsc)$/i, '') + '.ffpfsc';
  };

  // Source on PS5 FTP: stage to local mkpfs work dir before mkpfs runs.
  // We can't run mkpfs over FTP directly because mkpfs seeks back to patch
  // headers in the output file, which FTP doesn't support cleanly. Both
  // pack-file (single file) and pack-folder (recursive download) are
  // supported - pack-folder downloads the whole game directory tree first,
  // which can be slow on big games (tens of GB). For unpack the source is
  // always a single file, even for big .ffpfsc images.
  if (source_ftp && (source_ftp.ip || source_ftp.path)) {
    if (!source_ftp.ip) return { error: 'source_ftp.ip required' };
    if (!source_ftp.path || !source_ftp.path.trim()) return { error: 'source_ftp.path required' };
    const ftpPath = source_ftp.path.startsWith('/') ? source_ftp.path : '/' + source_ftp.path;
    const remoteBase = path.posix.basename(ftpPath.replace(/\/+$/, ''));
    if (!remoteBase) return { error: 'source_ftp.path missing filename' };
    if (mode === 'unpack' && !UNPACKABLE_EXT_RE.test(remoteBase)) {
      return { error: 'Unpack expects .ffpfsc/.ffpfs/.pfs/.dat/.bin' };
    }
    if (mode === 'exfat-unpack' && !EXFAT_EXT_RE.test(remoteBase)) {
      return { error: 'exFAT unpack expects a .exfat image' };
    }
    const baseName = params.output_name && params.output_name.trim()
      ? params.output_name.trim()
      : defaultOutName(remoteBase);
    // The eventual local source path is filled in during execute when we
    // have the job id; for now we return a placeholder so the queue display
    // shows something sensible.
    return {
      src: `ftp://${source_ftp.ip}${ftpPath}`,
      src_ftp: { ip: source_ftp.ip, path: ftpPath, name: remoteBase, is_dir: mode === 'pack-folder' || mode === 'exfat-folder' },
      out: path.join(mkpfsWorkDir, baseName),
      baseName,
      stat: null,
    };
  }

  // Source on an SMB share: smbclient streams the file to a local staging
  // dir before mkpfs runs (same reason as FTP — mkpfs needs a seekable local
  // file). Currently used by the kebab-menu "Unpack now/queue" actions on
  // SMB-hosted .ffpfsc images so the user no longer has to do a manual
  // Import step first.
  if (source_smb && (source_smb.source_id != null || source_smb.path != null)) {
    const sid = Number(source_smb.source_id);
    if (!Number.isFinite(sid) || sid <= 0) return { error: 'source_smb.source_id required' };
    const smbRow = getSource(sid);
    if (!smbRow || smbRow.type !== 'smb') return { error: `SMB source ${sid} not found` };
    const rawPath = String(source_smb.path || '').replace(/\\/g, '/').replace(/^\/+/, '');
    if (!rawPath) return { error: 'source_smb.path required' };
    const remoteBase = path.posix.basename(rawPath.replace(/\/+$/, ''));
    if (!remoteBase) return { error: 'source_smb.path missing filename' };
    if (mode === 'unpack' && !UNPACKABLE_EXT_RE.test(remoteBase)) {
      return { error: 'Unpack expects .ffpfsc/.ffpfs/.pfs/.dat/.bin' };
    }
    if (mode === 'exfat-unpack' && !EXFAT_EXT_RE.test(remoteBase)) {
      return { error: 'exFAT unpack expects a .exfat image' };
    }
    const baseName = params.output_name && params.output_name.trim()
      ? params.output_name.trim()
      : defaultOutName(remoteBase);
    return {
      src: `smb://${smbRow.smb_host}/${smbRow.smb_share}/${rawPath}`,
      src_smb: {
        source_id: sid,
        source_name: smbRow.name,
        path: rawPath,
        name: remoteBase,
        is_dir: mode === 'pack-folder' || mode === 'exfat-folder',
      },
      out: path.join(mkpfsWorkDir, baseName),
      baseName,
      stat: null,
    };
  }

  if (!source_path) return { error: 'source_path, source_ftp or source_smb required' };
  const isAbsolute = source_path.startsWith('/');
  let src;
  if (isAbsolute) {
    src = path.resolve(source_path);
    if (!isLocalPathAllowed(src)) return { error: `Path not allowed: ${src}` };
  } else {
    src = path.resolve(mkpfsWorkDir, source_path.replace(/\\/g, '/').replace(/^\/+/, ''));
    if (!isInsideWorkDir(src)) return { error: 'Source must be within work dir' };
  }
  if (!fs.existsSync(src)) return { error: 'Source not found' };
  const stat = fs.statSync(src);
  if (mode === 'pack-file' && !stat.isFile()) return { error: 'Source must be a file for pack-file mode' };
  if (mode === 'pack-folder' && !stat.isDirectory()) return { error: 'Source must be a directory for pack-folder mode' };
  if (mode === 'exfat-file' && !stat.isFile()) return { error: 'Source must be a file for exfat-file mode' };
  if (mode === 'exfat-folder' && !stat.isDirectory()) return { error: 'Source must be a directory for exfat-folder mode' };
  if (mode === 'unpack') {
    if (!stat.isFile()) return { error: 'Source must be a .ffpfsc file for unpack mode' };
    if (!UNPACKABLE_EXT_RE.test(src)) return { error: 'Unpack expects .ffpfsc/.ffpfs/.pfs/.dat/.bin' };
  }
  if (mode === 'exfat-unpack') {
    if (!stat.isFile()) return { error: 'Source must be a .exfat file for exfat-unpack mode' };
    if (!EXFAT_EXT_RE.test(src)) return { error: 'exFAT unpack expects a .exfat image' };
  }
  const baseName = params.output_name && params.output_name.trim()
    ? params.output_name.trim()
    : defaultOutName(path.basename(src));
  const out = path.join(path.dirname(src), baseName);
  return { src, stat, out, baseName };
}

function buildConvertJob(params, validated) {
  const jobId = newJobId();
  // When the source lives on PS5 FTP we default push targets to the same
  // PS5 + same directory so the result lands next to the original. The user
  // can still override push_ip / push_dest from the form.
  const fromFtp = !!validated.src_ftp;
  const defaultPushIp = fromFtp ? validated.src_ftp.ip : null;
  const defaultPushDest = fromFtp
    ? (path.posix.dirname(validated.src_ftp.path) || '/data/homebrew')
    : '/data/homebrew';
  return {
    id: jobId,
    mode: params.mode || 'pack-file',
    status: 'running',
    command: '',
    source: validated.src,
    source_ftp: validated.src_ftp || null,
    source_smb: validated.src_smb || null,
    output: validated.out,
    log: '',
    progress: 0,
    started_at: new Date().toISOString(),
    finished_at: null,
    exit_code: null,
    error: null,
    pid: null,
    // FTP-sourced jobs imply auto-push (the staged copy alone is useless on
    // the manager box), so flip push_after on by default in that case.
    // Unpack modes produce folders, not single image files — pushing folders
    // back to PS5 FTP isn't supported in this code path, so always opt out.
    push_after: (params.mode === 'unpack' || params.mode === 'exfat-unpack') ? false
      : (fromFtp ? (params.push_after !== false) : !!params.push_after),
    push_ip: params.push_ip || defaultPushIp,
    push_dest: params.push_dest || defaultPushDest,
    delete_source_after: !!params.delete_source_after,
    _params: params,
  };
}

// Walk a remote FTP directory tree and return the cumulative byte total.
// Used so the progress bar during pack-folder staging can show real %.
async function ftpFolderSize(client, remoteDir) {
  let total = 0;
  const entries = await client.list(remoteDir);
  for (const e of entries) {
    if (e.name === '.' || e.name === '..') continue;
    const isDir = e.type === 2 || e.isDirectory === true;
    const child = path.posix.join(remoteDir, e.name);
    if (isDir) total += await ftpFolderSize(client, child);
    else if (typeof e.size === 'number') total += e.size;
  }
  return total;
}

async function stageFromPs5Ftp(job) {
  if (!job.source_ftp) return;
  const ftp = loadFtp();
  const stageDir = path.join(getDiskTmpRoot(), 'ftp-stage', job.id);
  fs.mkdirSync(stageDir, { recursive: true });
  const isDir = !!job.source_ftp.is_dir || job.mode === 'pack-folder' || job.mode === 'exfat-folder';
  const localSrc = path.join(stageDir, job.source_ftp.name);
  appendLog(job, `[manager] Staging ${isDir ? 'folder' : 'file'} ${job.source_ftp.path} from ${job.source_ftp.ip} → ${localSrc}\n`);
  job.status = 'staging';
  let lastPct = -1;
  await withFtp(job.source_ftp.ip, ftp, async (client) => {
    // Resolve total size for progress reporting.
    try {
      if (isDir) {
        appendLog(job, `[manager] Walking remote folder to compute total size…\n`);
        const total = await ftpFolderSize(client, job.source_ftp.path);
        if (Number.isFinite(total) && total > 0) {
          job._stageTotal = total;
          appendLog(job, `[manager] Remote folder size: ${(total / 1024 / 1024).toFixed(1)} MB\n`);
        }
      } else {
        const size = await client.size(job.source_ftp.path);
        if (Number.isFinite(size) && size > 0) job._stageTotal = size;
      }
    } catch (e) {
      appendLog(job, `[manager] size probe warning: ${e.message} (progress %% may be inaccurate)\n`);
    }
    client.trackProgress(info => {
      if (!job._stageTotal || !info.bytes) return;
      const pct = Math.min(99, Math.floor((info.bytes / job._stageTotal) * 100));
      if (pct !== lastPct) {
        lastPct = pct;
        job.progress = pct; // staging eats the 0..99 progress band
      }
    });
    try {
      if (isDir) {
        // basic-ftp.downloadToDir mirrors the remote tree into localSrc.
        fs.mkdirSync(localSrc, { recursive: true });
        await client.downloadToDir(localSrc, job.source_ftp.path);
      } else {
        await client.downloadTo(localSrc, job.source_ftp.path);
      }
    } finally {
      client.trackProgress();
    }
  });
  job.source = localSrc;
  job._ftpStagedPath = localSrc;
  job._ftpStageDir = stageDir;
  const localStat = fs.statSync(localSrc);
  const sizeStr = localStat.isDirectory()
    ? `directory ${localSrc}`
    : `${localStat.size} bytes`;
  appendLog(job, `[manager] Stage complete (${sizeStr}). Running mkpfs…\n`);
  job.progress = 0; // reset for mkpfs phase
}

function cleanupFtpStage(job) {
  if (!job._ftpStageDir) return;
  try {
    fs.rmSync(job._ftpStageDir, { recursive: true, force: true });
    appendLog(job, `[manager] Cleaned up FTP stage dir ${job._ftpStageDir}\n`);
  } catch (e) {
    appendLog(job, `[manager] Failed to clean FTP stage dir: ${e.message}\n`);
  }
  job._ftpStageDir = null;
  job._ftpStagedPath = null;
}

// SMB staging: mkpfs needs a local seekable file, so we shell out to
// smbclient and pull the source into a per-job temp dir before mkpfs runs.
// Symmetric with stageFromPs5Ftp(); the FTP version uses basic-ftp and has
// stream progress hooks, while smbclient is one shot so we report the final
// size only.
// Probe a remote SMB file size via `smbclient ls`. Used to drive the staging
// progress bar — smbclient itself doesn't emit progress lines we can parse.
// Returns 0 when the size can't be resolved (rare; we fall back to an
// indeterminate display in that case).
async function smbProbeFileSize(src, remotePath) {
  try {
    const dir = path.posix.dirname(remotePath);
    const name = path.posix.basename(remotePath);
    const args = [...buildSmbArgs(src), '-c', dir && dir !== '.' ? `cd "${dir}"; ls "${name}"` : `ls "${name}"`];
    const { stdout } = await runSmbClient(args);
    // Output line example:
    //   "  some-file.ffpfsc  A 25324355584  Tue Jun  2 17:54:07 2026"
    const escaped = name.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    const m = stdout.match(new RegExp(`^\\s{2}${escaped}\\s+[ADHRSN]*\\s+(\\d+)`, 'm'));
    return m ? Number(m[1]) : 0;
  } catch (_) { return 0; }
}

async function stageFromSmb(job) {
  if (!job.source_smb) return;
  const src = getSource(job.source_smb.source_id);
  if (!src || src.type !== 'smb') {
    throw new Error(`SMB source ${job.source_smb.source_id} not found`);
  }
  const stageDir = path.join(getDiskTmpRoot(), 'smb-stage', job.id);
  fs.mkdirSync(stageDir, { recursive: true });
  const isDir = !!job.source_smb.is_dir || job.mode === 'pack-folder' || job.mode === 'exfat-folder';
  const remotePath = job.source_smb.path.replace(/^\/+/, '');
  const remoteName = job.source_smb.name;
  const localSrc = path.join(stageDir, remoteName);
  appendLog(job, `[manager] Staging ${isDir ? 'folder' : 'file'} smb://${src.smb_host}/${src.smb_share}/${remotePath} → ${localSrc}\n`);
  job.status = 'staging';
  job.phase = 'smb-staging';
  job.progress = 0;
  job.bytes_written = 0;
  job.bytes_total = 0;

  // Probe remote file size so the staging % bar is meaningful. Folders skip
  // this — they would require a recursive size walk we don't have a quick
  // smbclient incantation for; the bar just goes indeterminate instead.
  let totalSize = 0;
  if (!isDir) {
    totalSize = await smbProbeFileSize(src, remotePath);
    if (totalSize > 0) {
      job.bytes_total = totalSize;
      appendLog(job, `[manager] Remote file size: ${(totalSize / 1024 / 1024).toFixed(1)} MB\n`);
    } else {
      appendLog(job, `[manager] WARNING: could not probe remote size; staging bar will be indeterminate\n`);
    }
  }

  // Poll the staged destination size while smbclient transfers. smbclient
  // doesn't emit progress lines we can hook into, so this is the only honest
  // signal we have. Same field names as the mkpfs unpack poller so the queue
  // UI doesn't need a separate code path.
  const stagePoller = setInterval(() => {
    try {
      let size = 0;
      if (isDir) {
        size = pathSizeBytes(localSrc);
      } else if (fs.existsSync(localSrc)) {
        size = fs.statSync(localSrc).size;
      }
      job.bytes_written = size;
      if (totalSize > 0) {
        job.progress = Math.min(99, Math.max(1, Math.round((size / totalSize) * 100)));
      }
    } catch (_) { /* file may not exist yet */ }
  }, 1500);

  try {
    if (isDir) {
      // smbclient recursive get into a local dir. `prompt OFF; recurse ON; mget *`
      // is the standard incantation; we cd into the source directory first.
      fs.mkdirSync(localSrc, { recursive: true });
      const args = [
        ...buildSmbArgs(src),
        '-c', `prompt OFF; recurse ON; lcd "${localSrc}"; cd "${remotePath}"; mget *`,
      ];
      const { stdout, code } = await runSmbClient(args);
      const e = smbClientError(stdout, code);
      if (e) throw new Error(`SMB stage (folder): ${e.message}`);
    } else {
      const args = [
        ...buildSmbArgs(src),
        '-c', `get "${remotePath}" "${localSrc}"`,
      ];
      const { stdout, code } = await runSmbClient(args);
      const e = smbClientError(stdout, code);
      if (e) throw new Error(`SMB stage: ${e.message}`);
    }
  } finally {
    clearInterval(stagePoller);
  }

  if (!fs.existsSync(localSrc)) {
    throw new Error(`SMB stage produced no file at ${localSrc}`);
  }
  job.source = localSrc;
  job._smbStagedPath = localSrc;
  job._smbStageDir = stageDir;
  const st = fs.statSync(localSrc);
  const sizeStr = st.isDirectory() ? `directory ${localSrc}` : `${st.size} bytes`;
  appendLog(job, `[manager] Stage complete (${sizeStr}). Running mkpfs…\n`);
  // Reset for the mkpfs phase — runUnpack swaps in its own polling loop.
  job.progress = 0;
  job.bytes_written = 0;
}

function cleanupSmbStage(job) {
  if (!job._smbStageDir) return;
  try {
    fs.rmSync(job._smbStageDir, { recursive: true, force: true });
    appendLog(job, `[manager] Cleaned up SMB stage dir ${job._smbStageDir}\n`);
  } catch (e) {
    appendLog(job, `[manager] Failed to clean SMB stage dir: ${e.message}\n`);
  }
  job._smbStageDir = null;
  job._smbStagedPath = null;
}

async function executeConvertJob(job) {
  const params = job._params || {};
  const opts = {
    compress: params.compress !== false,
    verify: params.verify !== false,
    version: params.version || 'PS5',
    block_size: params.block_size,
    compression_level: params.compression_level,
    case_sensitive: !!params.case_sensitive,
    skip_executable_compression: !!params.skip_executable_compression,
    signed: !!params.signed,
    require_game_files: !!params.require_game_files,
  };

  // If source is on PS5 FTP, download it to a staging dir first. Errors here
  // fail the job before mkpfs is even invoked.
  if (job.source_ftp) {
    try {
      await stageFromPs5Ftp(job);
    } catch (e) {
      appendLog(job, `[manager] FTP stage failed: ${e.message}\n`);
      job.status = 'failed';
      job.error = `FTP stage failed: ${e.message}`;
      job.finished_at = new Date().toISOString();
      cleanupFtpStage(job);
      return;
    }
  }

  // Same dance for SMB sources — smbclient pulls the file/folder into a
  // per-job temp dir so mkpfs can read it as a local seekable file.
  if (job.source_smb) {
    try {
      await stageFromSmb(job);
    } catch (e) {
      appendLog(job, `[manager] SMB stage failed: ${e.message}\n`);
      job.status = 'failed';
      job.error = `SMB stage failed: ${e.message}`;
      job.finished_at = new Date().toISOString();
      cleanupSmbStage(job);
      return;
    }
  }

  // Always overwrite existing output - mkpfs may refuse to write over an
  // existing .ffpfsc or extraction directory.
  if (job.output && fs.existsSync(job.output)) {
    try {
      const outStat = fs.statSync(job.output);
      if (outStat.isDirectory()) {
        fs.rmSync(job.output, { recursive: true, force: true });
      } else {
        fs.unlinkSync(job.output);
      }
      appendLog(job, `[manager] Removed existing output ${path.basename(job.output)} (overwrite)\n`);
    } catch (e) {
      appendLog(job, `[manager] WARNING: could not remove existing output: ${e.message}\n`);
    }
  }
  let result;
  try {
    if (job.mode === 'pack-file') result = await runPackFile(job, opts);
    else if (job.mode === 'pack-folder') {
      // PS5 PFS reader requires game files at the image root, no wrapper
      // folder. If the user picked an outer dir whose only child is the
      // real game root, transparently promote so the resulting .ffpfsc
      // is mountable. See resolveGameRootForPack().
      const resolved = resolveGameRootForPack(job.source);
      if (resolved.message) appendLog(job, `[manager] ${resolved.message}\n`);
      if (resolved.promoted) {
        job.original_source = job.source;
        job.source = resolved.root;
      }
      result = await runPackFolder(job, opts);
    }
    else if (job.mode === 'unpack') {
      // unpack-specific options live alongside the generic pack opts.
      const unpackOpts = {
        ekpfs_key: params.ekpfs_key,
        new_crypt: !!params.new_crypt,
      };
      result = await runUnpack(job, unpackOpts);
    }
    else if (job.mode === 'exfat-file' || job.mode === 'exfat-folder') {
      // exFAT image builder — lib/exfat.js does the mkfs.exfat + loop-mount
      // + rsync dance. The same appendLog / updateProgressFromText pipeline
      // is wired through so phase + progress surface in the Tasks UI.
      result = await createExfatImage(job, { appendLog, updateProgressFromText }, job.source, job.output, {
        volume_label: params.volume_label,
        size_bytes: params.size_bytes,
      });
    }
    else if (job.mode === 'exfat-unpack') {
      result = await unpackExfatImage(job, { appendLog, updateProgressFromText }, job.source, job.output);
    }
  } catch (e) {
    result = { code: -1, error: e.message };
  }

  job.exit_code = result.code;
  job.finished_at = new Date().toISOString();
  if (result.code === 0) {
    job.status = 'completed';
    // Force the progress/phase to their "finished" terminal values. Without
    // this the unpack poller leaves progress at 95 and phase='extracting'
    // (because we cap the synthesised progress in startOutputDirPoller to
    // 95%) and the queue UI then shows "Completed · 95% · extracting"
    // forever. Pack jobs get 100% from mkpfs' own '100%' line via
    // updateProgressFromText, but they leave behind whatever 'phase' the
    // poller last set, so this also acts as a cleanup for them.
    job.progress = 100;
    job.phase = null;
    job.unpack_phase = null;
    log('info', `mm job ${job.id} completed (${job.mode}): ${path.basename(job.output)}`);

    if (job.delete_source_after && job.source && fs.existsSync(job.source) && !job._ftpStagedPath && !job._smbStagedPath) {
      // Never delete a staged file from the "source" path: that's our own
      // scratch copy (FTP or SMB stage). delete_source_after only applies
      // to genuine local sources picked by the user.
      try {
        if (fs.statSync(job.source).isDirectory()) fs.rmSync(job.source, { recursive: true, force: true });
        else fs.unlinkSync(job.source);
        appendLog(job, `\n[manager] Deleted source ${path.basename(job.source)}\n`);
      } catch (e) {
        appendLog(job, `\n[manager] Failed to delete source: ${e.message}\n`);
      }
    }
    await runPushAfter(job);
    cleanupFtpStage(job);
    cleanupSmbStage(job);
  } else if (job.status === 'cancelled') {
    log('info', `mm job ${job.id} cancelled`);
    cleanupFtpStage(job);
    cleanupSmbStage(job);
  } else {
    job.status = 'failed';
    job.error = result.error || `exit ${result.code}`;
    log('error', `mm job ${job.id} failed (${job.mode}): exit ${result.code}`);
    cleanupFtpStage(job);
    cleanupSmbStage(job);
  }
}

