function spawnIntoJob(job, cmd, args, opts = {}) {
  return new Promise((resolve) => {
    appendLog(job, `\n[manager] $ ${cmd} ${args.join(' ')}\n`);
    const proc = spawn(cmd, args, opts);
    job._proc = proc;
    job.pid = proc.pid;
    proc.stdout.on('data', d => appendLog(job, d));
    proc.stderr.on('data', d => appendLog(job, d));
    proc.on('error', err => {
      appendLog(job, `[manager] error: ${err.message}\n`);
      resolve({ code: -1, error: err.message });
    });
    proc.on('close', code => resolve({ code }));
  });
}

// Recursive size of a path. Returns 0 if the path doesn't exist. Used for the
// unpack progress poller (mkpfs is silent so we can't parse stdout for %).
function pathSizeBytes(p) {
  if (!p) return 0;
  let st;
  try { st = fs.statSync(p); } catch (_) { return 0; }
  if (st.isFile()) return st.size;
  if (!st.isDirectory()) return 0;
  let total = 0;
  const stack = [p];
  while (stack.length) {
    const cur = stack.pop();
    let entries;
    try { entries = fs.readdirSync(cur); } catch (_) { continue; }
    for (const name of entries) {
      const full = path.join(cur, name);
      let s;
      try { s = fs.statSync(full); } catch (_) { continue; }
      if (s.isDirectory()) stack.push(full);
      else total += s.size;
    }
  }
  return total;
}

// mkpfs unpack emits *zero* output (no stdout, no stderr, no --verbose flag)
// and has two phases that look identical from outside:
//   1) Scan: read the entire compressed image sequentially to build the inode
//      tree. Can take many minutes on multi-GB images; nothing is written.
//   2) Extract: walk the tree and dump files into the output directory.
// Without instrumentation the user just sees "running 0%" the whole time.
// We sidestep that by polling /proc/<pid>/io for the syscall counters (rchar
// = bytes the program asked to read) and the output dir size, and surface
// both as live metrics + a 2-phase synthetic progress.
function readProcIo(pid) {
  if (!pid) return null;
  try {
    const txt = fs.readFileSync(`/proc/${pid}/io`, 'utf8');
    const out = {};
    for (const line of txt.split('\n')) {
      const m = line.match(/^(\w+):\s+(\d+)$/);
      if (m) out[m[1]] = Number(m[2]);
    }
    return out;
  } catch (_) { return null; }
}

function startOutputDirPoller(job, sourceSize) {
  if (job._outputPoller) return;
  job.bytes_read = 0;
  job.bytes_written = 0;
  job.bytes_total = sourceSize || 0;
  job.phase = 'scanning';
  // Keep the legacy field around for one release in case any consumer still
  // reads it — both point at the same value.
  job.unpack_phase = 'scanning';
  const intervalMs = 2500;
  job._outputPoller = setInterval(() => {
    if (!['running', 'pushing', 'starting'].includes(job.status)) return;
    // Phase / progress derivation:
    //   - Bytes written > 0 → extract phase. Show output-dir size and map it
    //     to a 50–95% bar (extract usually takes about as long as scan).
    //   - Otherwise → scan phase. Use rchar/source_size, capped at 50%.
    // Either way we keep ≥1% so the queue UI never sticks on exactly zero
    // while the process is genuinely alive.
    const bw = pathSizeBytes(job.output);
    job.bytes_written = bw;
    const io = readProcIo(job.pid);
    if (io && io.rchar != null) job.bytes_read = io.rchar;

    if (bw > 0) {
      job.phase = 'extracting';
      job.unpack_phase = 'extracting';
      if (sourceSize > 0) {
        const ratio = bw / sourceSize;
        job.progress = Math.min(95, Math.max(50, 50 + Math.round(ratio * 45)));
      } else {
        job.progress = Math.max(job.progress || 1, 50);
      }
    } else if (io && io.rchar != null && sourceSize > 0) {
      job.phase = 'scanning';
      job.unpack_phase = 'scanning';
      const ratio = io.rchar / sourceSize;
      job.progress = Math.min(50, Math.max(1, Math.round(ratio * 50)));
    } else if (job.progress == null || job.progress === 0) {
      job.progress = 1;
    }
  }, intervalMs);
}

function stopOutputDirPoller(job) {
  if (job._outputPoller) {
    clearInterval(job._outputPoller);
    job._outputPoller = null;
  }
  job.bytes_written = pathSizeBytes(job.output);
}

async function runPushAfter(job) {
  if (!job.push_after || !job.push_ip) return;
  appendLog(job, `\n[manager] Pushing ${path.basename(job.output)} to ${job.push_ip}:${job.push_dest} via FTP...\n`);
  job.status = 'pushing';
  try {
    const ftp = loadFtp();
    await uploadFileResilient(job.push_ip, ftp, job.output, job.push_dest, path.basename(job.output), {
      onLog: (line) => appendLog(job, line),
      onProgress: ({ bytes, total }) => {
        if (total > 0) job.progress = Math.min(100, Math.round((bytes / total) * 100));
      },
    });
    appendLog(job, `[manager] Push OK\n`);
    job.status = 'completed';
  } catch (e) {
    appendLog(job, `[manager] Push failed: ${e.message}\n`);
    job.status = 'push_failed';
    job.error = e.message;
  }
}

