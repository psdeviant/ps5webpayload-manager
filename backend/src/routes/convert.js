async function withFtp(ip, ftpOpts, fn) {
  const client = new FtpClient(15000);
  client.ftp.verbose = false;
  try {
    await client.access({
      host: ip,
      port: ftpOpts.port,
      user: ftpOpts.username || 'anonymous',
      password: ftpOpts.password || '',
      secure: false,
    });
    // Tune the control socket so a sleepy router / PS5 doesn't silently kill us.
    try { client.ftp.socket.setKeepAlive(true, 15_000); } catch (_) {}
    try { client.ftp.socket.setNoDelay(true); } catch (_) {}
    return await fn(client);
  } finally {
    try { client.close(); } catch (_) {}
  }
}

