// In-place upgrade (or downgrade/pin) of mkpfs inside the running app
// container. Because the venv at /app/.venv is owned by the runtime user
// (1000:1000), pip can write to it without root. No image rebuild and no
// docker socket required — the new mkpfs binary is live the moment the
// command finishes. The image still picks up the same upgrade on the
// next `docker compose build` thanks to the >=MIN floor in Dockerfile.
router.post('/mkpfs/upgrade', async (req, res) => {
  const target = (req.body && typeof req.body.version === 'string')
    ? req.body.version.trim()
    : '';
  const pipSpec = target ? `mkpfs==${target}` : 'mkpfs';

  const stages = [
    [MKPFS_PIP, ['install', '--no-cache-dir', '--upgrade', pipSpec]],
    // Fallbacks for non-venv (dev) setups where MKPFS_PIP defaults to
    // system pip3 and needs --break-system-packages on newer Debian.
    ['pip3', ['install', '--break-system-packages', '--no-cache-dir', '--upgrade', pipSpec]],
    ['pip3', ['install', '--no-cache-dir', '--upgrade', pipSpec]],
  ];

  // Skip duplicate stage 0 if MKPFS_PIP === pip3 already.
  const tried = [];
  let lastResult = null;
  for (const [bin, args] of stages) {
    if (tried.some(t => t.bin === bin && JSON.stringify(t.args) === JSON.stringify(args))) {
      continue;
    }
    tried.push({ bin, args });
    lastResult = await new Promise((resolve) => {
      const p = spawn(bin, args, { env: { ...process.env, PIP_DISABLE_PIP_VERSION_CHECK: '1' } });
      let stdout = '';
      let stderr = '';
      p.stdout.on('data', d => { stdout += d.toString(); });
      p.stderr.on('data', d => { stderr += d.toString(); });
      p.on('error', (e) => resolve({ ok: false, stdout, stderr: stderr + e.message, code: -1, bin, args }));
      p.on('close', (code) => resolve({ ok: code === 0, stdout, stderr, code, bin, args }));
    });
    log(lastResult.ok ? 'info' : 'warn', `[mkpfs upgrade] ${bin} ${args.join(' ')} → code=${lastResult.code}`);
    if (lastResult.ok) break;
  }

  if (!lastResult || !lastResult.ok) {
    return res.status(500).json({
      ok: false,
      error: 'pip install failed',
      attempts: tried.map(t => `${t.bin} ${t.args.join(' ')}`),
      stdout: lastResult?.stdout || '',
      stderr: lastResult?.stderr || '',
      code: lastResult?.code,
    });
  }

  // Re-probe so the UI gets the fresh version without an extra poll.
  // Also drop the PyPI cache so update_available recomputes.
  mkpfsPypiCache = { ts: 0, version: null, error: null };
  const newVersion = await getInstalledMkpfsVersion();
  const pypi = await fetchLatestMkpfsVersion(true).catch(() => ({ version: null }));

  res.json({
    ok: true,
    version: newVersion,
    latest_version: pypi.version || null,
    update_available: !!(newVersion && pypi.version) && compareSemver(newVersion, pypi.version) < 0,
    used_command: `${lastResult.bin} ${lastResult.args.join(' ')}`,
    stdout: lastResult.stdout,
    stderr: lastResult.stderr,
  });
});

