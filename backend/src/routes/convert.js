// Per-job log buffer ceiling. Each finished job sits in the `jobs` Map
// until evicted (see jobs-eviction interval at the bottom of this file);
// 2000 lines × ~80 chars × ~50 historical jobs added up to ~8 MB resident
// across long-running deployments. 800 lines covers a normal mkpfs /
// exfat / extract run end-to-end (the tail-side noise is the interesting
// bit anyway) and keeps the per-job ceiling under ~64 KB.
const MAX_LOG_LINES = 800;
const jobs = new Map();

// How long a finished job is kept in memory after it transitions to a
// terminal status (completed / failed / cancelled / push_failed).
// 15 minutes is plenty for the user to inspect the log + retry; the queue
// item itself stays attached to the queue array independently, so
// removing the underlying job just frees the log buffer + cached fields.
const JOBS_TERMINAL_TTL_MS = 15 * 60 * 1000;
const TERMINAL_STATUSES = new Set(['completed', 'failed', 'cancelled', 'push_failed']);

function evictExpiredJobs() {
  const now = Date.now();
  for (const [id, job] of jobs) {
    if (!TERMINAL_STATUSES.has(job.status)) continue;
    const finishedAt = job.finished_at ? Date.parse(job.finished_at) : 0;
    if (!finishedAt) continue;
    if (now - finishedAt > JOBS_TERMINAL_TTL_MS) {
      jobs.delete(id);
    }
  }
}
// Sweep every 5 minutes — cheap enough we don't bother debouncing on
// status transitions. unref() so this timer never keeps the event loop
// alive during shutdown.
setInterval(evictExpiredJobs, 5 * 60 * 1000).unref?.();

// Cached PyPI lookup so we don't hammer pypi.org every time the Convert
// tab is opened (status endpoint is polled on every render). 10 minutes
// is plenty — PyPI releases are weekly/monthly at most.
const MKPFS_PYPI_TTL_MS = 10 * 60 * 1000;
let mkpfsPypiCache = { ts: 0, version: null, error: null };

async function fetchLatestMkpfsVersion(force = false) {
  const now = Date.now();
  if (!force && mkpfsPypiCache.version && (now - mkpfsPypiCache.ts) < MKPFS_PYPI_TTL_MS) {
    return { version: mkpfsPypiCache.version, cached: true, error: null };
  }
  try {
    const r = await fetch('https://pypi.org/pypi/mkpfs/json', {
      headers: { 'User-Agent': 'p5-manager/mkpfs-update-check' },
      signal: AbortSignal.timeout(5000),
    });
    if (!r.ok) throw new Error(`pypi http ${r.status}`);
    const j = await r.json();
    const v = j?.info?.version || null;
    if (!v) throw new Error('no version field in pypi response');
    mkpfsPypiCache = { ts: Date.now(), version: v, error: null };
    return { version: v, cached: false, error: null };
  } catch (e) {
    mkpfsPypiCache = { ts: Date.now(), version: mkpfsPypiCache.version, error: e.message };
    return { version: mkpfsPypiCache.version, cached: !!mkpfsPypiCache.version, error: e.message };
  }
}

function compareSemver(a, b) {
  if (!a || !b) return 0;
  const pa = String(a).split(/[^0-9]+/).filter(Boolean).map(Number);
  const pb = String(b).split(/[^0-9]+/).filter(Boolean).map(Number);
  const n = Math.max(pa.length, pb.length);
  for (let i = 0; i < n; i++) {
    const x = pa[i] || 0;
    const y = pb[i] || 0;
    if (x !== y) return x < y ? -1 : 1;
  }
  return 0;
}

function getInstalledMkpfsVersion() {
  return new Promise((resolve) => {
    const proc = spawn(MKPFS_BIN, ['-V']);
    let out = '';
    proc.stdout.on('data', (d) => { out += d.toString(); });
    proc.stderr.on('data', (d) => { out += d.toString(); });
    proc.on('error', () => resolve(null));
    proc.on('close', () => {
      const m = out.match(/(\d+\.\d+\.\d+(?:[\w.+-]*)?)/);
      resolve(m ? m[1] : null);
    });
  });
}

function newJobId() { return crypto.randomBytes(8).toString('hex'); }

function appendLog(job, chunk) {
  const text = chunk.toString();
  job.log += text;
  const lines = job.log.split('\n');
  if (lines.length > MAX_LOG_LINES) {
    job.log = lines.slice(-MAX_LOG_LINES).join('\n');
  }
  updateProgressFromText(job, text);
}

function updateProgressFromText(job, text) {
  if (!text) return;
  // grab the last percentage figure seen in the chunk (works for 7z -bsp1, unrar, mkpfs --verbose)
  const matches = text.match(/(\d{1,3})\s*%/g);
  if (matches && matches.length) {
    const last = matches[matches.length - 1];
    const n = parseInt(last);
    if (!isNaN(n) && n >= 0 && n <= 100) {
      job.progress = n;
    }
    return;
  }
  // fallback: "X / Y" style counters (mkpfs blocks etc.)
  const ratio = text.match(/(\d+)\s*\/\s*(\d+)/);
  if (ratio) {
    const a = parseInt(ratio[1]);
    const b = parseInt(ratio[2]);
    if (b > 0 && a >= 0 && a <= b) {
      job.progress = Math.round((a / b) * 100);
    }
  }
}

function listMkpfsFiles(dir, base = '') {
  const entries = [];
  if (!fs.existsSync(dir)) return entries;
  for (const name of fs.readdirSync(dir)) {
    const full = path.join(dir, name);
    let stat;
    try { stat = fs.statSync(full); } catch (_) { continue; }
    const rel = path.posix.join(base, name);
    if (stat.isDirectory()) {
      entries.push({ name, path: rel, isDir: true });
    } else {
      entries.push({ name, path: rel, isDir: false, size: stat.size, mtime: stat.mtimeMs });
    }
  }
  return entries.sort((a, b) => Number(b.isDir) - Number(a.isDir) || a.name.localeCompare(b.name));
}

