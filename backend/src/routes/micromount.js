function runSmbClient(args, input) {
  return new Promise((resolve, reject) => {
    const proc = spawn('smbclient', args);
    let stdout = '';
    let stderr = '';
    proc.stdout.on('data', d => { stdout += d.toString(); });
    proc.stderr.on('data', d => { stderr += d.toString(); });
    proc.on('error', reject);
    proc.on('close', (code) => {
      resolve({ stdout, stderr, code });
    });
    if (input) {
      proc.stdin.write(input);
      proc.stdin.end();
    }
  });
}

function smbClientError(out, code) {
  const m = out.match(/(NT_STATUS_(?!OK\b)[A-Z_]+)/);
  if (m) {
    const status = m[1];
    const msg = status === 'NT_STATUS_OBJECT_NAME_NOT_FOUND' ? 'Path not found'
      : status === 'NT_STATUS_ACCESS_DENIED' ? 'Access denied'
      : status === 'NT_STATUS_LOGON_FAILURE' ? 'Authentication failed (check username/password)'
      : status === 'NT_STATUS_BAD_NETWORK_NAME' ? 'Share not found'
      : status === 'NT_STATUS_HOST_UNREACHABLE' || status === 'NT_STATUS_CONNECTION_REFUSED' ? 'Host unreachable'
      : status === 'NT_STATUS_NO_SUCH_FILE' ? 'No matching files'
      : status;
    return { status, message: msg };
  }
  if (code !== 0) return { status: 'EXIT', message: out.trim().split('\n').slice(-3).join(' ') || `smbclient exit ${code}` };
  return null;
}

function buildSmbArgs(src) {
  const target = `//${src.smb_host}/${src.smb_share}`;
  const args = [target];
  if (src.smb_password) args.push(src.smb_password);
  if (src.smb_username) args.push('-U', src.smb_domain ? `${src.smb_domain}\\${src.smb_username}` : src.smb_username);
  else args.push('-N');
  return args;
}

function parseSmbList(output) {
  const files = [];
  const lines = output.split('\n');
  for (const line of lines) {
    const m = line.match(/^\s{2}(.+?)\s{2,}([DAHRSN]*)\s+(\d+)\s+(.+)$/);
    if (m) {
      const name = m[1].trim();
      const attrs = m[2];
      const size = parseInt(m[3]);
      if (name === '.' || name === '..') continue;
      files.push({
        name,
        size,
        isDir: attrs.includes('D'),
      });
    }
  }
  return files;
}

// Helper: connect to the FTP server defined by a source row using the
// per-source credentials (NOT the global PS5 FTP creds). The source FTP can
// be any external FTP (NAS, seedbox, another PS5, etc.).
async function withSourceFtp(src, fn) {
  const client = new FtpClient(15000);
  client.ftp.verbose = false;
  try {
    await client.access({
      host: src.ftp_host,
      port: src.ftp_port || 21,
      user: src.ftp_username || 'anonymous',
      password: src.ftp_password || '',
      secure: false,
    });
    try { client.ftp.socket.setKeepAlive(true, 15_000); } catch (_) {}
    try { client.ftp.socket.setNoDelay(true); } catch (_) {}
    return await fn(client);
  } finally {
    try { client.close(); } catch (_) {}
  }
}

