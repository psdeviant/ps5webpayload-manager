function validateExtractParams(body) {
  const {
    source = 'smb',
    source_id,
    smb_path = '',
    filename,
    local_path,
    dest_subdir,
    dest_kind: destKindRaw,
    dest_local_path,
  } = body || {};

  if (source !== 'smb' && source !== 'local' && source !== 'local-fs') {
    return { error: 'source must be smb, local, or local-fs' };
  }

  let archivePath;
  let smbSource = null;
  let smbRemoteDir = '';

  ensureMkpfsDir();

  if (source === 'smb') {
    if (!source_id || !filename) return { error: 'source_id and filename required' };
    smbSource = getSource(parseInt(source_id));
    if (!smbSource || smbSource.type !== 'smb') return { error: 'SMB source not found' };
    smbRemoteDir = (smb_path || '').replace(/\\/g, '/').replace(/^\/+/, '').replace(/\/+$/, '');
  } else if (source === 'local-fs') {
    if (!local_path) return { error: 'local_path required' };
    archivePath = path.resolve(local_path);
    if (!isLocalPathAllowed(archivePath)) return { error: 'Path not allowed' };
    if (!fs.existsSync(archivePath) || !fs.statSync(archivePath).isFile()) return { error: 'Archive not found' };
  } else {
    if (!local_path) return { error: 'local_path required' };
    archivePath = path.resolve(mkpfsWorkDir, local_path.replace(/\\/g, '/').replace(/^\/+/, ''));
    if (!isInsideWorkDir(archivePath)) return { error: 'Out of work dir' };
    if (!fs.existsSync(archivePath)) return { error: 'Archive not found' };
  }

  const archiveName = source === 'smb' ? filename : path.basename(archivePath);
  const archiveType = detectArchiveType(archiveName);
  if (!archiveType) return { error: `Unsupported archive type: ${archiveName}` };

  const stripExt = (n) => n.replace(/\.(7z|rar|zip|tar\.gz|tgz|tar|r\d{2}|part\d+\.rar)$/i, '');

  let destKind = destKindRaw;
  if (!destKind) {
    if (source === 'smb') destKind = 'smb-back';
    else if (source === 'local-fs') destKind = 'local-fs';
    else destKind = 'workdir';
  }

  let destLabel;
  let plannedDestBase = null;
  if (destKind === 'smb-back') {
    if (source !== 'smb') return { error: 'smb-back destination requires source=smb' };
    destLabel = `smb://${smbSource.smb_host}/${smbSource.smb_share}${smbRemoteDir ? '/' + smbRemoteDir : ''}`;
  } else if (destKind === 'local-fs') {
    const baseDir = dest_local_path
      ? path.resolve(dest_local_path)
      : (source === 'local-fs' ? path.dirname(archivePath) : mkpfsWorkDir);
    if (!isLocalPathAllowed(baseDir)) return { error: `Path not allowed: ${baseDir}` };
    plannedDestBase = path.join(baseDir, stripExt(archiveName));
    destLabel = plannedDestBase;
  } else {
    plannedDestBase = dest_subdir
      ? path.resolve(mkpfsWorkDir, dest_subdir.replace(/\\/g, '/').replace(/^\/+/, ''))
      : path.join(mkpfsWorkDir, stripExt(archiveName));
    if (!isInsideWorkDir(plannedDestBase)) return { error: 'destination out of work dir' };
    destLabel = plannedDestBase;
  }

  return { source, smbSource, smbRemoteDir, archivePath, archiveName, archiveType, destKind, destLabel, plannedDestBase };
}

